void main() {
  //1. Integers
  int age = 20;
  print(age);

  //2. Double
  double pi = 3.142;
  print(pi);

  //3. String
  String name = "Jane Mwangi";
  print(name);

  //4.List
  List<String> names = ["Jane", "John", "James"];
  List<int> ages = [20, 30, 40];
  print(names);
  print(ages);

  //5. Maps
  Map<String, dynamic> person = {"name": "Jane", "age": 20, "isStudent": true};
  print(person);
}
